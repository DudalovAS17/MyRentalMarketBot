def build_main_menu_text(user) -> str:
    welcome_message = (
        "🏠 <b>Главное меню</b>\n\n "
        f"Здравствуйте, <b>{user.full_name or user.first_name or user.username or 'пользователь'}</b>!\n\n"
        "Выберите действие:"
    )
    return welcome_message
